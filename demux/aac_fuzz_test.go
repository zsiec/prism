package demux

import "testing"

func FuzzParseADTS(f *testing.F) {
	// Valid ADTS frame seed (7-byte header + payload)
	f.Add([]byte{0xFF, 0xF1, 0x4C, 0x80, 0x01, 0xA0, 0xFC, 0xDE, 0xAD, 0xBE, 0xEF, 0xCA, 0xFE})
	f.Add([]byte{0xFF, 0xF0, 0x50, 0x80, 0x02, 0x00, 0xFC, 0xAA, 0xBB, 0xDE, 0xAD})
	f.Add([]byte(nil))
	f.Fuzz(func(t *testing.T, data []byte) {
		ParseADTS(data) // must not panic
	})
}
